package Java_3rd_year.Bit_Masking;

public class Left_Shift {
    public static void main(String[] args) {
            int n=13;
            System.out.println(n<<1);
            int n1=-13;
            System.out.println(n1 <<1);
            byte b=-128;
            System.out.println(b<<1);                                                                                                                                                                                                                                                                                                                                                                                                     
    }
}
