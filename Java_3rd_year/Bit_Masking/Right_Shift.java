package Java_3rd_year.Bit_Masking;

public class Right_Shift {
    public static void main(String[] args) {

        int n=13;
        System.out.println(n>>1);
        
        int n1= -13;
        System.out.println(n1>>1);

        int a=-22;
        System.out.println(a>>1);  // -11

        int a1=-1;
        System.out.println(a1>>1);  // -1
    }
}
